% --- Executes on button press in togglebutton1.
function togglebutton1_Callback(hObject, eventdata, handles)
    global t;

    if get(hObject, 'value')
        tcp_str = get(handles.tcp, 'String');
        port_str = get(handles.port, 'String');
    
        tcpipClient = tcpip(tcp_str,str2num(port_str),'NetworkRole','Client');
        set(handles.figure1,'UserData',tcpipClient);
        
        set(tcpipClient,'OutputBufferSize',8*1); %设置缓存长度
        set(tcpipClient,'InputBufferSize',1024); %设置缓存长度
        set(tcpipClient,'Timeout',20); %设置连接时间为1分钟
        
        try
            fopen(tcpipClient);
        catch
            msgbox('TCP无法连接！');
            %set(hObject, 'value', 0);
            return;
        end
        set(hObject, 'String', '断开WIFI');
        t = timer('Period',0.001,'ExecutionMode','FixedRate','TimerFcn',{@PlotUpdate,handles});
        start(t);
    else
        tcpipClient = get(handles.figure1,'UserData');
        fclose(tcpipClient);
        delete(tcpipClient);
        stop(t);
        delete(t);
    set(hObject, 'String', '连接WIFI');
    end



